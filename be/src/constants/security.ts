/**
 * bcrypt 비밀번호 해싱에 사용되는 salt rounds 값
 * 
 * - 값: 10 = 2^10 = 1,024번의 해싱 반복
 * - 보안과 성능의 균형을 고려한 업계 권장값
 * - 무차별 대입 공격(Brute Force)에 대한 저항력 제공
 * 
 * 참고:
 * - 8: 낮은 보안 (개발/테스트용)
 * - 10: 권장값 (프로덕션 환경)
 * - 12: 높은 보안 (민감한 데이터)
 * - 14: 매우 높은 보안 (금융 등, 응답 시간 지연 가능)
 */
export const BCRYPT_SALT_ROUNDS = 10;

